# -*- encoding : utf-8 -*-
# ဟင်းချက်စရာ မှာခြင်း
# တွက်ချက်ခြင်းကို ဆောင်ရွက်ပေးသည့် ပရိုဂရမ်
# encoding: utf-8

@ကြက်သားတစ်ပိသာ = 35000
@လက်ကျန်ငွေ = 800
@စျေးနှုန်း = {"ဆန်" => 2000, "ဆီ" => 200, "ဆား" => 200}

def ဟင်းချက်စရာ
	if @လက်ကျန်ငွေ < @ကြက်သားတစ်ပိသာ
		puts "ကန်ဇွန်းရွက်တစ်စည်းပေးပါခင်ဗျာ"
	else
		puts "ကြက်သား တစ်ပိသာ ထည့်လိုက်စမ်း!!"
	end
end

def ငွေတွက်ခြင်း
	တစ်ပါတ်စာ = @စျေးနှုန်း.values.inject(:+)
end

puts "ဒီတစ်ပါတ်အတွက် ငွေ #{ငွေတွက်ခြင်း} ရှိမှ လောက်မယ်"
